#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

// --- CONFIGURATION ---
const COMPONENT_DIRS = [
  'src/design-system/components',
  'src/design-system/layouts',
  'src/design-system/compositions',
];
const OUTPUT_FILE = 'src/App.tsx';
const TOKENS_PATH = 'src/design-system/tokens.ts';

// --- HELPERS ---
function pascalCase(str) {
  return str
    .replace(/[-_ ]+(\w)/g, (_, c) => c.toUpperCase())
    .replace(/^\w/, c => c.toUpperCase());
}

function getComponentNames(dir) {
  const absDir = path.resolve(dir);
  if (!fs.existsSync(absDir)) return [];
  return fs
    .readdirSync(absDir, { withFileTypes: true })
    .filter(entry => entry.isDirectory())
    .map(entry => ({
      name: pascalCase(entry.name),
      category: path.basename(dir),
      importPath: `./design-system/${path.basename(dir)}/${entry.name}`
    }));
}

// --- MAIN ---
let allComponents = [];
COMPONENT_DIRS.forEach(dir => {
  allComponents = allComponents.concat(getComponentNames(dir));
});

const importStmts = allComponents.map(c => `import { ${c.name} } from '${c.importPath}';`);
const mappedEntries = allComponents.map(c => `  "${c.name}": ${c.name},`);
const categories = {
  Components: allComponents.filter(c => c.category === 'components').map(c => c.name),
  Layouts: allComponents.filter(c => c.category === 'layouts').map(c => c.name),
  Compositions: allComponents.filter(c => c.category === 'compositions').map(c => c.name),
};

const appTsx = `// AUTO-GENERATED by scripts/generate-showroom.cjs
import React, { useState } from 'react';
import { ThemeProvider, useTheme } from './design-system/ThemeProvider';
import { typography, darkTheme } from './design-system/tokens';
${importStmts.join('\n')}

const ALL_COMPONENTS = ${JSON.stringify(categories, null, 2)};
const MAPPED_COMPONENTS: Record<string, React.FC<any>> = {
${mappedEntries.join('\n')}
};

function Sidebar({ selected, onSelect }: { selected: string, onSelect: (n: string) => void }) {
  const { tokens } = useTheme();
  return (
    <nav style={{
      width: 280,
      background: tokens.canvasDefault,
      borderRight: \`1px solid \${tokens.modifiersMidlight}\`,
      height: 'calc(100vh - 64px)',
      overflowY: 'auto',
      position: 'fixed',
      top: 64,
      left: 0,
      padding: '32px 0'
    }}>
      {Object.entries(ALL_COMPONENTS).map(([category, items]) => (
        <div key={category} style={{ marginBottom: 32 }}>
          <div style={{
            fontSize: 11, fontWeight: 700, textTransform: 'uppercase', letterSpacing: '1.44px',
            color: tokens.foregroundDefault, opacity: 0.5, padding: '0 32px', marginBottom: 12,
            fontFamily: typography.fontFamilies.primary
          }}>
            {category}
          </div>
          {items.map(name => (
            <div
              key={name}
              onClick={() => onSelect(name)}
              style={{
                padding: '10px 32px', cursor: 'pointer', fontSize: 14,
                fontFamily: typography.fontFamilies.primary,
                color: selected === name ? tokens.accentPrimaryDefault : tokens.foregroundDefault,
                backgroundColor: selected === name ? tokens.elevationRaisedDefault : 'transparent',
                borderRight: selected === name ? \`3px solid \${tokens.accentPrimaryDefault}\` : '3px solid transparent',
                fontWeight: selected === name ? 600 : 400,
                transition: 'all 0.2s'
              }}
            >
              {name.replace(/([A-Z])/g, ' $1').trim()}
            </div>
          ))}
        </div>
      ))}
    </nav>
  );
}

function Topbar() {
  const { tokens, mode, toggleMode } = useTheme();
  return (
    <header style={{
      height: 64, borderBottom: \`1px solid \${tokens.modifiersMidlight}\`,
      backgroundColor: tokens.canvasDefault, display: 'flex', alignItems: 'center',
      justifyContent: 'space-between', padding: '0 32px', position: 'fixed', top: 0, left: 0, right: 0, zIndex: 100
    }}>
      <div style={{ display: 'flex', alignItems: 'center', gap: 12 }}>
        <div style={{ width: 28, height: 28, background: '#D60000', borderRadius: 4 }}></div>
        <span style={{ 
            fontWeight: 700, fontSize: 20, color: tokens.foregroundDefault,
            fontFamily: typography.fontFamilies.primary, letterSpacing: '-0.5px' 
        }}>
          LK Design GEL
        </span>
      </div>
      <button onClick={toggleMode} style={{
        background: 'transparent', border: \`1px solid \${tokens.modifiersMidlight}\`,
        padding: '8px 16px', borderRadius: 20, cursor: 'pointer', color: tokens.foregroundDefault,
        fontFamily: typography.fontFamilies.primary, fontWeight: 600, fontSize: 12
      }}>
        {mode === 'dark' ? 'LIGHT MODE' : 'DARK MODE'}
      </button>
    </header>
  );
}

function PageContent({ name }: { name: string }) {
  const { tokens } = useTheme();
  const [activeTab, setActiveTab] = useState('Design');
  const ComponentToRender = MAPPED_COMPONENTS[name];

  if (!ComponentToRender) return <div style={{ padding: 40 }}>Select a component</div>;

  return (
    <main style={{ marginLeft: 280, marginTop: 64, padding: '48px 64px', background: tokens.canvasDefault, minHeight: 'calc(100vh - 64px)' }}>
      <h1 style={{ fontSize: 48, fontWeight: 700, color: tokens.foregroundDefault, marginBottom: 40, fontFamily: typography.fontFamilies.primary }}>
        {name.replace(/([A-Z])/g, ' $1').trim()}
      </h1>

      <div style={{ display: 'flex', gap: 32, borderBottom: \`1px solid \${tokens.modifiersMidlight}\`, marginBottom: 48 }}>
        {['Design', 'Development', 'Accessibility'].map(tab => (
          <button
            key={tab}
            onClick={() => setActiveTab(tab)}
            style={{
              background: 'none', border: 'none', padding: '16px 0', fontSize: 16, cursor: 'pointer',
              color: activeTab === tab ? tokens.accentPrimaryDefault : tokens.foregroundDefault,
              borderBottom: activeTab === tab ? \`4px solid \${tokens.accentPrimaryDefault}\` : '4px solid transparent',
              fontWeight: activeTab === tab ? 600 : 400, opacity: activeTab === tab ? 1 : 0.6,
              fontFamily: typography.fontFamilies.primary
            }}
          >
            {tab}
          </button>
        ))}
      </div>

      {activeTab === 'Design' && (
        <div style={{
          background: tokens.elevationRaisedDefault, borderRadius: 12, padding: 64,
          border: \`2px dashed \${tokens.accentPrimaryDefault}\`, display: 'flex', justifyContent: 'center', alignItems: 'center'
        }}>
          <ComponentToRender>Previewing {name}</ComponentToRender>
        </div>
      )}

      {activeTab === 'Development' && (
        <pre style={{ background: tokens.elevationInsetDarker, padding: 32, borderRadius: 12, color: tokens.foregroundDefault, overflowX: 'auto' }}>
          <code>import { '{' } {name} { '}' } from '@lk/design-system';</code>
        </pre>
      )}
    </main>
  );
}

export default function App() {
  const [selected, setSelected] = useState(ALL_COMPONENTS.Components[0]);
  return (
    <ThemeProvider>
      <div style={{ backgroundColor: darkTheme.canvasDefault, minHeight: '100vh' }}>
        <Topbar />
        <Sidebar selected={selected} onSelect={setSelected} />
        <PageContent name={selected} />
      </div>
    </ThemeProvider>
  );
}
`;

fs.writeFileSync(OUTPUT_FILE, appTsx);
console.log('Successfully generated Lexus Showroom with Westpac structure.');